/// Tool execution result.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct ToolResult {
    /// Tool call identifier generated by the LLM.
    pub tool_call_id: String,
    /// Name of the tool that was requested.
    pub tool_name: String,
    /// Whether the tool succeeded.
    pub success: bool,
    /// Human-readable tool output.
    pub output: String,
    /// Typed failure classification for failed tool results.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub failure_class: Option<FailureClass>,
}

impl ToolResult {
    #[must_use]
    pub fn success(
        tool_call_id: impl Into<String>,
        tool_name: impl Into<String>,
        output: impl Into<String>,
    ) -> Self {
        Self {
            tool_call_id: tool_call_id.into(),
            tool_name: tool_name.into(),
            success: true,
            output: output.into(),
            failure_class: None,
        }
    }

    #[must_use]
    pub fn failure(
        tool_call_id: impl Into<String>,
        tool_name: impl Into<String>,
        output: impl Into<String>,
        failure_class: FailureClass,
    ) -> Self {
        Self {
            tool_call_id: tool_call_id.into(),
            tool_name: tool_name.into(),
            success: false,
            output: output.into(),
            failure_class: Some(failure_class),
        }
    }

    #[must_use]
    pub fn failure_classification(&self) -> Option<FailureClass> {
        if self.success {
            None
        } else {
            Some(self.failure_class.unwrap_or(FailureClass::Unknown))
        }
    }

    #[must_use]
    pub fn is_timeout(&self) -> bool {
        matches!(self.failure_classification(), Some(FailureClass::Timeout))
    }
}

/// The specific tool action that should be journaled for ripcord.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum JournalAction {
    FileWrite {
        path: PathBuf,
        snapshot_hash: Option<String>,
        size_bytes: u64,
        created: bool,
    },
    FileDelete {
        path: PathBuf,
        snapshot_hash: String,
    },
    FileMove {
        from: PathBuf,
        to: PathBuf,
    },
    GitCommit {
        repo: PathBuf,
        pre_ref: String,
        commit_sha: String,
    },
    GitBranchCreate {
        repo: PathBuf,
        branch: String,
    },
    GitPush {
        repo: PathBuf,
        remote: String,
        branch: String,
        pre_ref: String,
    },
    ShellCommand {
        command: String,
        exit_code: i32,
    },
    NetworkRequest {
        url: String,
        method: String,
        status_code: u16,
    },
}

impl JournalAction {
    /// Whether this action type can be mechanically reversed.
    pub fn is_reversible(&self) -> bool {
        matches!(
            self,
            Self::FileWrite { .. }
                | Self::FileDelete { .. }
                | Self::FileMove { .. }
                | Self::GitCommit { .. }
                | Self::GitBranchCreate { .. }
        )
    }
}

