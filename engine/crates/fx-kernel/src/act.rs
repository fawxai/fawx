/// Token accounting for loop steps that call an LLM.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Default)]
pub struct TokenUsage {
    /// Estimated input tokens consumed by prompts/context.
    pub input_tokens: u64,
    /// Estimated output tokens generated by the model.
    pub output_tokens: u64,
    /// Input tokens served from a provider prompt cache.
    pub cached_input_tokens: u64,
    /// Input tokens written into a provider prompt cache.
    pub cache_creation_input_tokens: u64,
}

impl TokenUsage {
    /// Total model-context tokens, excluding cache telemetry buckets.
    ///
    /// Provider cache fields are reported separately because some providers
    /// expose cached tokens as a subset of input tokens while others expose
    /// them as distinct billing buckets. Keeping this helper to input + output
    /// preserves the historical "conversation token total" contract and avoids
    /// accidental double counting.
    pub fn total_tokens(self) -> u64 {
        self.input_tokens.saturating_add(self.output_tokens)
    }

    /// Total usage telemetry across all reported provider buckets.
    pub fn telemetry_total_tokens(self) -> u64 {
        self.total_tokens()
            .saturating_add(self.cached_input_tokens)
            .saturating_add(self.cache_creation_input_tokens)
    }

    /// True when no provider-reported usage buckets are populated.
    pub fn is_empty(self) -> bool {
        self.input_tokens == 0
            && self.output_tokens == 0
            && self.cached_input_tokens == 0
            && self.cache_creation_input_tokens == 0
    }

    /// Construct token usage from provider-reported LLM usage.
    pub fn from_llm_usage(usage: Usage) -> Self {
        Self {
            input_tokens: u64::from(usage.input_tokens),
            output_tokens: u64::from(usage.output_tokens),
            cached_input_tokens: u64::from(usage.cached_input_tokens),
            cache_creation_input_tokens: u64::from(usage.cache_creation_input_tokens),
        }
    }

    /// Add another usage sample.
    pub fn accumulate(&mut self, other: TokenUsage) {
        self.input_tokens = self.input_tokens.saturating_add(other.input_tokens);
        self.output_tokens = self.output_tokens.saturating_add(other.output_tokens);
        self.cached_input_tokens = self
            .cached_input_tokens
            .saturating_add(other.cached_input_tokens);
        self.cache_creation_input_tokens = self
            .cache_creation_input_tokens
            .saturating_add(other.cache_creation_input_tokens);
    }
}

