/// A side effect outside the local workspace that a tool completed.
#[derive(Debug, Clone, Copy, Serialize, Deserialize, PartialEq, Eq, Hash)]
#[serde(rename_all = "snake_case")]
pub enum ExternalActionKind {
    /// A comment was posted to a GitHub pull request.
    GithubPrComment,
    /// A comment was posted to a GitHub issue.
    GithubIssueComment,
    /// A GitHub pull request review was submitted.
    GithubPrReview,
    /// Changes were pushed to a git remote.
    GitPush,
}

/// Typed evidence that an external side effect completed.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct ExternalActionEvidence {
    /// The kind of external action that completed.
    pub kind: ExternalActionKind,
    /// A stable URL for the completed action when the provider/tool exposes one.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub url: Option<String>,
    /// Git remote affected by a push action, when it can be determined.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub remote: Option<String>,
    /// Git push refspecs/branch targets observed for a push action.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub refspecs: Vec<String>,
}

impl ExternalActionEvidence {
    #[must_use]
    pub fn github_pr_comment(url: Option<String>) -> Self {
        Self {
            kind: ExternalActionKind::GithubPrComment,
            url,
            remote: None,
            refspecs: Vec::new(),
        }
    }

    #[must_use]
    pub fn github_issue_comment(url: Option<String>) -> Self {
        Self {
            kind: ExternalActionKind::GithubIssueComment,
            url,
            remote: None,
            refspecs: Vec::new(),
        }
    }

    #[must_use]
    pub fn github_pr_review(url: Option<String>) -> Self {
        Self {
            kind: ExternalActionKind::GithubPrReview,
            url,
            remote: None,
            refspecs: Vec::new(),
        }
    }

    #[must_use]
    pub fn git_push(remote: Option<String>, refspecs: Vec<String>) -> Self {
        Self {
            kind: ExternalActionKind::GitPush,
            url: None,
            remote,
            refspecs,
        }
    }
}

/// Structured diagnostics for a failed tool invocation.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum ToolExecutionDiagnostics {
    /// Diagnostics for a failed `run_command` invocation.
    RunCommand(RunCommandDiagnostics),
    /// Diagnostics preserved from a structured HTTP/tool failure.
    Http(HttpDiagnostics),
}

/// Diagnostics captured for a failed `run_command` invocation.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq, Default)]
pub struct RunCommandDiagnostics {
    /// Command exit code when the process exited normally.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub exit_code: Option<i32>,
    /// Short stderr sample for quick debugging.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub stderr_snippet: Option<String>,
    /// Total wall-clock duration of the command execution.
    pub duration_ms: u64,
    /// Whether the command was executed through a shell wrapper.
    pub shell: bool,
    /// Whether the command timed out before it could complete.
    pub timed_out: bool,
    /// Completed external actions observed from this command.
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub external_actions: Vec<ExternalActionEvidence>,
}

impl ToolExecutionDiagnostics {
    #[must_use]
    pub fn as_run_command(&self) -> Option<&RunCommandDiagnostics> {
        match self {
            Self::RunCommand(diagnostics) => Some(diagnostics),
            Self::Http(_) => None,
        }
    }

    #[must_use]
    pub fn as_http(&self) -> Option<&HttpDiagnostics> {
        match self {
            Self::RunCommand(_) => None,
            Self::Http(diagnostics) => Some(diagnostics),
        }
    }

    #[must_use]
    pub fn as_metadata_value(&self) -> serde_json::Value {
        let mut value = serde_json::to_value(self).unwrap_or_else(|_| {
            serde_json::json!({
                "kind": "unavailable",
            })
        });
        redact_external_action_urls(&mut value);
        value
    }

    #[must_use]
    pub fn duration_ms(&self) -> Option<u64> {
        match self {
            Self::RunCommand(diagnostics) => Some(diagnostics.duration_ms),
            // Structured HTTP diagnostics do not yet carry wall-clock duration.
            Self::Http(_) => None,
        }
    }

    #[must_use]
    pub fn timed_out(&self) -> bool {
        match self {
            Self::RunCommand(diagnostics) => diagnostics.timed_out,
            Self::Http(_) => false,
        }
    }

    #[must_use]
    pub fn external_actions(&self) -> &[ExternalActionEvidence] {
        match self {
            Self::RunCommand(diagnostics) => &diagnostics.external_actions,
            Self::Http(_) => &[],
        }
    }
}

/// Infer typed external-action evidence from a successful `run_command` result.
///
/// This keeps legacy shell command compatibility at the tool boundary. The
/// loop can then consume a typed evidence record instead of re-parsing tool
/// output as part of the root-turn contract.
#[must_use]
pub fn external_actions_from_run_command(
    command: &str,
    output: &str,
) -> Vec<ExternalActionEvidence> {
    let normalized = normalize_shell_command(command);
    let mut actions = Vec::new();

    if command_posts_github_pr_comment_with_output(command, output) {
        actions.push(ExternalActionEvidence::github_pr_comment(
            github_comment_url(output),
        ));
    }
    if command_posts_github_issue_comment(&normalized) {
        // GitHub issue comments and PR comments share the same
        // `#issuecomment-...` URL shape, so the common extractor is
        // intentionally used for both evidence types.
        actions.push(ExternalActionEvidence::github_issue_comment(
            github_comment_url(output),
        ));
        // GitHub PR comments are issue comments under the hood; preserve the
        // legacy contract where `gh issue comment <pr-number>` satisfied a
        // user request to comment on a PR.
        if !actions
            .iter()
            .any(|action| action.kind == ExternalActionKind::GithubPrComment)
        {
            actions.push(ExternalActionEvidence::github_pr_comment(
                github_comment_url(output),
            ));
        }
    }
    if command_submits_github_pr_review(&normalized) {
        actions.push(ExternalActionEvidence::github_pr_review(
            github_comment_url(output),
        ));
        if normalized.contains(" --comment ")
            && !actions
                .iter()
                .any(|action| action.kind == ExternalActionKind::GithubPrComment)
        {
            actions.push(ExternalActionEvidence::github_pr_comment(
                github_comment_url(output),
            ));
        }
    }
    if command_pushes_git(&normalized) {
        let (remote, refspecs) = git_push_metadata(command);
        actions.push(ExternalActionEvidence::git_push(remote, refspecs));
    }

    actions
}

/// Infer typed external-action evidence from a `run_command` argument object.
///
/// `run_command` accepts both legacy `command` strings and exact `argv`
/// invocations. The root-turn contract should treat those shapes identically:
/// both are command executions and both can complete external actions.
#[must_use]
pub fn external_actions_from_run_command_arguments(
    arguments: &serde_json::Value,
    output: &str,
) -> Vec<ExternalActionEvidence> {
    run_command_argument_texts(arguments)
        .into_iter()
        .find_map(|command| {
            let evidence = external_actions_from_run_command(&command, output);
            (!evidence.is_empty()).then_some(evidence)
        })
        .unwrap_or_default()
}

fn run_command_argument_texts(arguments: &serde_json::Value) -> Vec<String> {
    let mut commands = Vec::new();
    if let Some(command) = arguments
        .get("command")
        .and_then(serde_json::Value::as_str)
        .map(str::trim)
        .filter(|command| !command.is_empty())
    {
        commands.push(command.to_string());
    }

    if let Some(argv) = arguments
        .get("argv")
        .and_then(serde_json::Value::as_array)
        .map(|argv| run_command_argv_text(argv))
        .filter(|command| !command.trim().is_empty())
    {
        commands.push(argv);
    }
    commands
}

fn run_command_argv_text(argv: &[serde_json::Value]) -> String {
    argv.iter()
        .filter_map(serde_json::Value::as_str)
        .collect::<Vec<_>>()
        .join(" ")
}

fn command_posts_github_pr_comment_with_output(command: &str, output: &str) -> bool {
    // This is intentionally a coarse legacy-shell recognizer, not a shell
    // parser. `run_command` still supports arbitrary shell strings, aliases,
    // functions, and pipelines, so the durable contract is the typed evidence
    // emitted by the tool. The normalization here only preserves compatibility
    // for older paths where typed diagnostics are absent.
    let normalized = normalize_shell_command(command);
    normalized.contains(" gh pr comment ")
        || normalized.contains(" gh issue comment ")
        || (normalized.contains(" gh api ")
            && normalized.contains("/issues/")
            && normalized.contains("/comments")
            && command_posts_with_gh_api(&normalized))
        || (normalized.contains(" gh api graphql ")
            && normalized.contains("addcomment")
            && command_posts_with_gh_api(&normalized)
            && output_contains_github_comment_url(output))
}

fn command_posts_github_issue_comment(normalized: &str) -> bool {
    normalized.contains(" gh issue comment ")
        || (normalized.contains(" gh api ")
            && normalized.contains("/issues/")
            && normalized.contains("/comments")
            && command_posts_with_gh_api(normalized))
}

fn command_submits_github_pr_review(normalized: &str) -> bool {
    normalized.contains(" gh pr review ")
        && (normalized.contains(" --approve ")
            || normalized.contains(" --request-changes ")
            || normalized.contains(" --comment "))
}

fn command_pushes_git(normalized: &str) -> bool {
    normalized.contains(" git push ")
}

fn git_push_metadata(command: &str) -> (Option<String>, Vec<String>) {
    let tokens = command.split_whitespace().collect::<Vec<_>>();
    let Some(push_index) = tokens.windows(2).position(|window| {
        window[0].eq_ignore_ascii_case("git") && window[1].eq_ignore_ascii_case("push")
    }) else {
        return (None, Vec::new());
    };

    let mut positionals = Vec::new();
    let mut repository_from_flag = false;
    let mut index = push_index + 2;
    while index < tokens.len() {
        let token = tokens[index];
        if token == "--repo" || token == "--repository" {
            repository_from_flag = true;
            index = index.saturating_add(2);
            continue;
        }
        if token.starts_with("--repo=") || token.starts_with("--repository=") {
            repository_from_flag = true;
            index += 1;
            continue;
        }
        if matches!(token, "-o" | "--push-option" | "--receive-pack" | "--exec") {
            index = index.saturating_add(2);
            continue;
        }
        if token.starts_with("--push-option=")
            || token.starts_with("--receive-pack=")
            || token.starts_with("--exec=")
        {
            index += 1;
            continue;
        }
        if token.starts_with('-') {
            index += 1;
            continue;
        }
        positionals.push(token.to_string());
        index += 1;
    }

    if repository_from_flag {
        return (None, positionals);
    }

    let remote = positionals.first().cloned();
    let refspecs = positionals.into_iter().skip(1).collect();
    (remote, refspecs)
}

fn command_posts_with_gh_api(normalized_command: &str) -> bool {
    normalized_command.contains(" -x post ")
        || normalized_command.contains(" --method post ")
        || normalized_command.contains(" -f ")
        || normalized_command.contains(" --field ")
        || normalized_command.contains(" --raw-field ")
}

fn output_contains_github_comment_url(output: &str) -> bool {
    github_comment_url(output).is_some()
}

fn github_comment_url(output: &str) -> Option<String> {
    output.split_whitespace().find_map(|token| {
        let trimmed =
            token.trim_matches(|ch: char| matches!(ch, '"' | '\'' | ')' | ']' | '}' | ',' | ';'));
        let normalized = trimmed.to_ascii_lowercase();
        if normalized.contains("github.com/")
            && (normalized.contains("/pull/") || normalized.contains("/issues/"))
            && normalized.contains("#issuecomment-")
        {
            Some(trimmed.to_string())
        } else {
            None
        }
    })
}

fn normalize_shell_command(command: &str) -> String {
    // Collapse whitespace and lowercase so substring checks are insensitive to
    // line wrapping and command casing. We deliberately keep punctuation and
    // quoting untouched: this helper should not pretend to understand shell
    // syntax, it only makes the fallback recognizer stable enough for common
    // GitHub CLI forms.
    let collapsed = command.split_whitespace().collect::<Vec<_>>().join(" ");
    format!(" {} ", collapsed.to_ascii_lowercase())
}

fn redact_external_action_urls(value: &mut serde_json::Value) {
    let Some(actions) = value
        .get_mut("external_actions")
        .and_then(serde_json::Value::as_array_mut)
    else {
        return;
    };
    for action in actions {
        if let Some(object) = action.as_object_mut() {
            if object.contains_key("url") {
                object.insert("url".to_string(), serde_json::json!("<redacted>"));
            }
        }
    }
}

/// Controls parallel execution of tool calls.
#[derive(Debug, Clone, Default)]
pub struct ConcurrencyPolicy {
    /// Maximum number of tool calls to execute in parallel.
    /// `None` means unlimited (all calls run concurrently).
    pub max_parallel: Option<NonZeroUsize>,
    /// Per-tool-call timeout. If a call exceeds this, it returns
    /// an error result (not a panic).
    pub timeout_per_call: Option<std::time::Duration>,
}

/// Declares whether a tool's results may be cached for identical inputs.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ToolCacheability {
    /// Results are deterministic and safe to cache within a cycle.
    Cacheable,
    /// Results should never be cached unless explicitly opted in.
    NeverCache,
    /// Tool has side effects and may invalidate cache entries.
    SideEffect,
}

/// Classifies the effect of a specific tool invocation.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ToolCallClassification {
    /// The invocation only observes existing state.
    Observation,
    /// The invocation coordinates work owned by the kernel but does not mutate
    /// the user's workspace or external resources itself.
    Orchestration,
    /// The invocation may mutate state or trigger side effects.
    Mutation,
}

impl From<ToolCallClassification> for SignalToolClassification {
    fn from(classification: ToolCallClassification) -> Self {
        match classification {
            ToolCallClassification::Observation | ToolCallClassification::Orchestration => {
                Self::Observation
            }
            ToolCallClassification::Mutation => Self::Mutation,
        }
    }
}

impl From<SignalToolClassification> for ToolCallClassification {
    fn from(classification: SignalToolClassification) -> Self {
        match classification {
            SignalToolClassification::Observation => Self::Observation,
            SignalToolClassification::Mutation => Self::Mutation,
        }
    }
}

