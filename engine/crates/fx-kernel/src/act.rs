    #[test]
    fn run_command_diagnostics_metadata_redacts_external_action_urls() {
        let url = "https://github.com/fawxai/fawx/pull/1854#issuecomment-1";
        let issue_url = "https://github.com/fawxai/fawx/issues/1855#issuecomment-1";
        let diagnostics = ToolExecutionDiagnostics::RunCommand(RunCommandDiagnostics {
            exit_code: Some(0),
            stderr_snippet: None,
            duration_ms: 42,
            shell: true,
            timed_out: false,
            external_actions: vec![
                ExternalActionEvidence::github_pr_comment(Some(url.to_string())),
                ExternalActionEvidence::github_issue_comment(Some(issue_url.to_string())),
                ExternalActionEvidence::github_pr_review(None),
                ExternalActionEvidence::git_push(
                    Some("origin".to_string()),
                    vec!["dev".to_string()],
                ),
            ],
        });

        let metadata = diagnostics.as_metadata_value();

        assert_eq!(
            metadata["external_actions"][0]["url"],
            serde_json::json!("<redacted>")
        );
        assert_eq!(
            metadata["external_actions"][1]["url"],
            serde_json::json!("<redacted>")
        );
        assert!(metadata["external_actions"][2].get("url").is_none());
        assert!(metadata["external_actions"][3].get("url").is_none());
        assert_eq!(metadata["external_actions"][3]["remote"], "origin");
        assert_eq!(metadata["external_actions"][3]["refspecs"][0], "dev");
        assert_eq!(
            diagnostics.external_actions()[0].url.as_deref(),
            Some(url),
            "redaction should only affect serialized metadata, not control-plane evidence"
        );
    }

